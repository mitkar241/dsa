"""
Tuples are entities in Python that work almost similar to that of lists,
but differ in the main feature from lists,
is in that they are inmutable.
"""

# Defining and Initializing a tuple called example
example = ("First", "Second", "Third", "Fourth")
print(example)
print(example[1:3])

"""
Type Converting between Tuples, Lists, and Strings:
"""
# Convert list to a tuple
tuple(['first', 'second', 'third'])
# Convert tuple to a list
list(('first', 'second', 'third'))
# Convert string to a list
list("mitkar241")
